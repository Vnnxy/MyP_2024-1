/**
 * Class for the individual cabins..
 * 
 * @author equipo.
 */
public class Individual extends Cabin {

    /**
     * Credit getter.
     * 
     * @return double the credit of the cabin.
     */
    public double getCredit() {
        return 500;
    }

    /**
     * Weight getter.
     * 
     * @return double the weight of the cabin.
     */
    public double getWeight() {
        return 10;
    }

    /**
     * Speed getter.
     * 
     * @return double the speed of the cabin.
     */
    public double getSpeed() {
        return 5;
    }

    /**
     * Name getter.
     * 
     * @return String the name of the component cabin.
     */
    public String getName() {
        return "One pilot";
    }
}
