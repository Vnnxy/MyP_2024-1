public class CustomShip extends StarForge {

    public void addJetEngine(JetEngine engine) {
        parts.add(engine);
    }

    public void addArmor(Armor armor) {
        parts.add(armor);
    }

    public void addWeapon(Weapons weapon) {
        parts.add(weapon);
    }

    public void addCabin(Cabin cabin) {
        parts.add(cabin);
    }
}
