/**
 * Class for the InterContinental Engine..
 * 
 * @author equipo.
 */
public class InterContinental extends JetEngine {

    /**
     * Credit getter.
     * 
     * @return double the credit of the engine.
     */
    public double getCredit() {
        return 200;
    }

    /**
     * Speed getter.
     * 
     * @return double the speed of the engine.
     */
    public double getSpeed() {
        return 600;
    }

    /**
     * Weight getter.
     * 
     * @return double the weight of the engine.
     */
    public double getWeight() {
        return 1500;
    }

    /**
     * Name getter.
     * 
     * @return String the name of the component engine.
     */
    public String getName() {
        return "InterContinental";
    }
}
