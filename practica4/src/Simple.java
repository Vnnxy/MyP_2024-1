/**
 * Class for the Simple armor..
 * 
 * @author equipo.
 */
public class Simple extends Armor {

    /**
     * Credit getter.
     * 
     * @return double the credit of the armor.
     */
    public double getCredit() {
        return 700;
    }

    /**
     * Defense getter.
     * 
     * @return double the speed of the armor.
     */
    public double getDefense() {
        return 70;
    }

    /**
     * Weight getter.
     * 
     * @return double the weight of the armor.
     */
    public double getWeight() {
        return 120;
    }

    /**
     * Speed getter.
     * 
     * @return double the speed of the armor.
     */
    public double getSpeed() {
        return -20;
    }

    /**
     * Name getter.
     * 
     * @return String the name of the component armor.
     */
    public String getName() {
        return "Simple";
    }
}
