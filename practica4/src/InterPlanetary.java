/**
 * Class for the InterPlanetary Engine..
 * 
 * @author equipo.
 */
public class InterPlanetary extends JetEngine {

    /**
     * Credit getter.
     * 
     * @return double the credit of the engine.
     */
    public double getCredit() {
        return 750;
    }

    /**
     * Speed getter.
     * 
     * @return double the speed of the engine.
     */
    public double getSpeed() {
        return 2500;
    }

    /**
     * Weight getter.
     * 
     * @return double the weight of the engine.
     */
    public double getWeight() {
        return 3500;
    }

    /**
     * Name getter.
     * 
     * @return String the name of the component engine.
     */
    public String getName() {
        return "InterPlanetary";
    }
}
