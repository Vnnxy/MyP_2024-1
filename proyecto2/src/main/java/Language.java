/**
 * Interface that represents the language of the gui.
 * 
 * @author equipo.
 */

public interface Language {

    /**
     * Message for change language menu item
     * 
     * @return the message
     */
    public String changeItem();

    /**
     * Message for connect  menu item
     * 
     * @return the message
     */
    public String connectItem();

    /**
     * Message for connect menu
     * 
     * @return the message
     */
    public String connectMenu();

    /**
     * Message for the view and edit button
     * 
     * @return the message
     */
    public String editButton();

    /**
     * Message for the exit menu
     * 
     * @return the message
     */
    public String exitMenu();

    /**
     * Message for the language menu
     * 
     * @return the message
     */
    public String languageMenu();

    /**
     * Message for the load button
     * 
     * @return the message
     */
    public String loadButton();

    /**
     * Message for logout  menu item
     * 
     * @return the message
     */
    public String logoutItem();

    /**
     * Message for the welcome label
     * 
     * @return the message
     */
    public String welcomeText();

    /**
     * Message for the address label
     * 
     * @return the message
     */
    public String addressLabel();

    /**
     * Message for the connect button
     * 
     * @return the message
     */
    public String connectButton();

    /**
     * Message for the port label
     * 
     * @return the message
     */
    public String portLabel();

    /**
     * Message for the return button
     * 
     * @return the message
     */
    public String returnButton();

    /**
     * Message for the language accept button 
     * 
     * @return the message
     */
    public String languageButton();

    /**
     * Message for the language label
     * 
     * @return the message
     */
    public String languageLabel();

    /**
     * Message to accept a file button
     * 
     * @return the message
     */
    public String fileButton();

    /**
     * Message to change language info label
     * 
     * @return the message
     */
    public String infoLabel();

    /**
     * Message to return from language menu 
     * 
     * @return the message
     */
    public String returnLButton();

    /**
     * Message for the edit button in the text editor
     * 
     * @return the message
     */
    public String editTButton();

    /**
     * Message for the save button
     * 
     * @return the message
     */
    public String saveButton();
    
}
